print("CONVERSOR DE GRAUS");

#variavel que recebe o valor dos graus em celsius 
climateCelsius = float(input("Informe os graus em Celsius:"));

# variavel para converter celsius para fahrenheit
fahrenheit = ((climateCelsius * 9)/ 5) + 32

# imprimindo o resultado na tela
print("Em fahrenheit esta: ",fahrenheit," graus");